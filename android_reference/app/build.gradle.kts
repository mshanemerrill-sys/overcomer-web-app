// Generate .env file from environment variables dynamically
val envFile = file("${rootDir}/.env")
val envExampleFile = file("${rootDir}/.env.example")
if (envExampleFile.exists()) {
    val envLines = mutableListOf<String>()
    envExampleFile.readLines().forEach { line ->
        if (line.trim().startsWith("#") || line.trim().isEmpty()) {
            envLines.add(line)
        } else {
            val parts = line.split("=", limit = 2)
            if (parts.size == 2) {
                val key = parts[0].trim()
                val envValue = System.getenv(key)
                if (envValue != null && envValue.isNotEmpty()) {
                    envLines.add("$key=$envValue")
                } else {
                    envLines.add(line)
                }
            } else {
                envLines.add(line)
            }
        }
    }
    envFile.writeText(envLines.joinToString("\n"))
    println("GENERATED_ENV_PATH: ${envFile.absolutePath}, exists: ${envFile.exists()}")
}

